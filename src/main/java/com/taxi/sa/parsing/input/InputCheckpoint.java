package com.taxi.sa.parsing.input;


public class InputCheckpoint {

    private float price;
    private int x1, y1, x2, y2;

    public InputCheckpoint() {
    }

    public float getPrice() {
        return price;
    }

    public int getX1() {
        return x1;
    }

    public int getY1() {
        return y1;
    }

    public int getX2() {
        return x2;
    }

    public int getY2() {
        return y2;
    }

}


